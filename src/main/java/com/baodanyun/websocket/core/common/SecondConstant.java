package com.baodanyun.websocket.core.common;


public class SecondConstant {

    /**
     * 1秒
     */
    public static final int ONE_SECOND = 1;

    /**
     * 3秒
     */
    public static final int THREE_SECONDS = 3*ONE_SECOND;

    /**
     * 10秒
     */
    public static final int TEN_SECONDS = 10*ONE_SECOND;


    /**
     * 60秒
     */
    public static final int ONE_MIN_SECONDS = 60;

    /**
     * 3分钟
     */
    public static final int THREE_MIN_SECONDS = 3 * ONE_MIN_SECONDS;

    /**
     * 5分钟
     */
    public static final int FIVE_MIN_SECONDS = 5 * ONE_MIN_SECONDS;

    /**
     * 10分钟
     */
    public static final int TEN_MIN_SECONDS = 10 * ONE_MIN_SECONDS;

    /**
     * 半小时
     */
    public static final int HALF_HOUR_SECONDS = 3 * TEN_MIN_SECONDS;

    /**
     * 1小时
     */
    public static final int ONE_HOUR_SECONDS = 2 * HALF_HOUR_SECONDS;

    /**
     * 12小时
     */
    public static final int TWELVE_HOUR_SECONDS = 12 * ONE_HOUR_SECONDS;

    /**
     * 24小时
     */
    public static final int ONE_DAY_SECONDS = 2 * TWELVE_HOUR_SECONDS;

    /**
     * 7天
     */
    public static final int ONE_WEEK_SECONDS = 7 * ONE_DAY_SECONDS;

    /**
     * 一月，最大时间
     */
    public static final int ONE_MONTH_SECONDS = 60*60*24*30;

}
